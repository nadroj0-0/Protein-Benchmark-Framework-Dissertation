#!/usr/bin/env bash

set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=_homology_cluster_common.sh
source "$SCRIPT_DIR/_homology_cluster_common.sh"

DIAGNOSTIC_PILOT=0
FIXTURE_MODE=0
[[ -z "${ATTRITION_OVERRIDE:-}" ]] || launcher_error \
    "An identity-specific attrition override cannot be applied array-wide"
unset ATTRITION_OVERRIDE
require_file ATTRITION_POLICY
require_file PILOT_APPROVAL
require_file PILOT_COMPLETION_MARKER
require_file PILOT_ATTRITION_REPORT
require_file PILOT_TASK_CONTEXT
require_file PILOT_MEASUREMENT_EVIDENCE
require_value PILOT_RUN_DIR
[[ -d "$PILOT_RUN_DIR" ]] || launcher_error "PILOT_RUN_DIR is not a directory: $PILOT_RUN_DIR"
validate_common_contract
EXPECTED_ATTRITION_POLICY_SHA256="$(file_sha256 "$ATTRITION_POLICY")"
EXPECTED_PILOT_APPROVAL_SHA256="$(file_sha256 "$PILOT_APPROVAL")"
EXPECTED_PILOT_COMPLETION_MARKER_SHA256="$(file_sha256 "$PILOT_COMPLETION_MARKER")"
EXPECTED_PILOT_ATTRITION_REPORT_SHA256="$(file_sha256 "$PILOT_ATTRITION_REPORT")"
EXPECTED_PILOT_TASK_CONTEXT_SHA256="$(file_sha256 "$PILOT_TASK_CONTEXT")"
EXPECTED_PILOT_MEASUREMENT_EVIDENCE_SHA256="$(file_sha256 "$PILOT_MEASUREMENT_EVIDENCE")"
export ATTRITION_POLICY PILOT_APPROVAL PILOT_RUN_DIR PILOT_COMPLETION_MARKER
export PILOT_ATTRITION_REPORT PILOT_TASK_CONTEXT PILOT_MEASUREMENT_EVIDENCE
export EXPECTED_ATTRITION_POLICY_SHA256 EXPECTED_PILOT_APPROVAL_SHA256
export EXPECTED_PILOT_COMPLETION_MARKER_SHA256 EXPECTED_PILOT_ATTRITION_REPORT_SHA256
export EXPECTED_PILOT_TASK_CONTEXT_SHA256 EXPECTED_PILOT_MEASUREMENT_EVIDENCE_SHA256
export UNIPROT_SOURCE_SCOPE FRAMEWORK_REVISION FROZEN_INPUT_MANIFEST
export UNIREF90_FASTA UNIREF90_FASTA_SHA256 IDMAPPING IDMAPPING_SHA256
export GOA GOA_SHA256 GO_OBO GO_OBO_SHA256 RESULTS_ROOT EXPECTED_MMSEQS_VERSION
export MMSEQS_BIN SPLIT_POLICY TRAINING_POPULATION SEED MIN_COUNT NO_DOWNLOADS RUN_ID
export REQUESTED_SLOTS FIXTURE_MODE DIAGNOSTIC_PILOT
export UNIPROT_RELEASE GOA_RELEASE ONTOLOGY_RELEASE EXPECTED_FROZEN_INPUT_MANIFEST_SHA256
[[ -z "${UNIPROT_SPROT_SEQUENCES:-}" ]] || export UNIPROT_SPROT_SEQUENCES UNIPROT_SPROT_SEQUENCES_SHA256
[[ -z "${UNIPROT_TREMBL_SEQUENCES:-}" ]] || export UNIPROT_TREMBL_SEQUENCES UNIPROT_TREMBL_SEQUENCES_SHA256
PYTHONPATH="$FRAMEWORK_ROOT/benchmark_builders/homology_cluster/src${PYTHONPATH:+:$PYTHONPATH}" \
    "$PYTHON_BIN" -m homology_cluster_benchmark authorize-array \
    --attrition-policy "$ATTRITION_POLICY" \
    --pilot-approval "$PILOT_APPROVAL" \
    --pilot-completion-marker "$PILOT_COMPLETION_MARKER" \
    --pilot-attrition-report "$PILOT_ATTRITION_REPORT" \
    --pilot-run-dir "$PILOT_RUN_DIR" \
    --pilot-task-context "$PILOT_TASK_CONTEXT" \
    --pilot-measurement-evidence "$PILOT_MEASUREMENT_EVIDENCE" \
    --frozen-input-manifest "$FROZEN_INPUT_MANIFEST" \
    --framework-revision "$FRAMEWORK_REVISION" \
    --uniprot-source-scope "$UNIPROT_SOURCE_SCOPE" \
    --split-policy "$SPLIT_POLICY" \
    --training-population "$TRAINING_POPULATION" \
    --expected-mmseqs-version "$EXPECTED_MMSEQS_VERSION" \
    --uniprot-release "$UNIPROT_RELEASE" \
    --goa-release "$GOA_RELEASE" \
    --ontology-release "$ONTOLOGY_RELEASE"

launch_array full-array 1-6
